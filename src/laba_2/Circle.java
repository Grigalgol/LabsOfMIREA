package laba_2;

public class Circle {
    private double x; //координата центра окружности по абциссе
    private double y; //координата центра окружности по ординате
    private double radius; //радиус окружности

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getRadius() {
        return radius;
    }

    public void setRadius(double radius) {
        this.radius = radius;
    }
}
